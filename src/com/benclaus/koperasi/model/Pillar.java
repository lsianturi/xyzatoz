package com.benclaus.koperasi.model;

public class Pillar {
	private int id;
	private String name;
	public Pillar() {
		
	}
	public Pillar(int id) {
		super();
		this.id = id;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
